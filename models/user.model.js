const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
    },
    password: {
        type: String,
        required: true,
        minlength: 6
    },
    role: {
        type: String,
        enum: ['user', 'admin'],
        default: 'user'
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    referrals: [{
    type: mongoose.Schema.Types.ObjectId,
    ref: 'candidateReferal'
  }]
});

const userModel = mongoose.model('User', userSchema);
 module.exports={userModel}